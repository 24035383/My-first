public class NumbersDemo {
    public static void main(String[] args){
    
        //Declare two integer variables and assign values
        int num1 = 4;
        int num2 = 7;
        
        //call the methods with each number
        displayTwiceTheNumber(num1);
        displayNumberPlusFive(num1);
        displayNumberSquared(num1);
        
        System.out.println();
        
        displayTwiceTheNumber(num2);
        displayNumberPlusFive(num2);
        displayNumberSquared(num2);
    
    }
   public static void displayTwiceTheNumber(int num){
   System.out.println("Twice"+ num+ "is"+(num*2));
   }
   
   public static void displayNumberPlusFive(int num){
   System.out.println("Plus five is"+ num+ "is"+(num+5));
   }
   
   public static void displayNumberSquared(int num){
   System.out.println("Squared is"+ num+ "is"+(num*num));
   }
}
